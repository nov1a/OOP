    private void nextButtonPanelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_nextButtonPanelMouseClicked
        // TODO add your handling code here:
//        
        ruta.getAlamat().setNegaraAtauProv(provinsiTextField.getText());
        ruta.getAlamat().setKabKota(kabKotaTextField.getText());
        ruta.getAlamat().setKecamatan(kecamatanTextField.getText());
        ruta.getAlamat().setDesaAtauKel(desaTextField.getText());
        ruta.getAlamat().setNoBlokSensus(nomorBSTextField.getText());
        ruta.getAlamat().setNoKodeSample(nomorKodeSampleTextField.getText());
        ruta.getAlamat().setNoUrutRtSample(nomorRutaSampleTextField.getText());
        if (getSelected(klasifikasiButtonGroup)!=null){
            ruta.getAlamat().setKlasifikasiDesa(getSelected(klasifikasiButtonGroup).getText());
            
        }
        ruta.setNamaKepalaRT(namaKaRuTaTextField.getText());
        ruta.setNoUrutPemberiInfo(pemberiInfoTextField.getText());
                if(getSelected(hasilKunjunganButtonGroup)!=null){
                        String temp = getSelected(hasilKunjunganButtonGroup).getText();
                        ruta.setHasilKunjungan(temp);
                        if(temp.equalsIgnoreCase("Menolak") || temp.equalsIgnoreCase("Tidak dapat ditemui")){
                           
                                JTextField jumlahART=new JTextField();
                                JTextField jumlahArtSepuluhKeatas=new JTextField();
                                JPanel modal = new JPanel(new GridLayout(0, 1));
                                modal.add(new JLabel("Jumlah anggota rumah tangga:"));
                                modal.add(jumlahART);
                                modal.add(new JLabel("Jumlah anggota rumah tangga diatas 10 tahun:"));
                                modal.add(jumlahArtSepuluhKeatas);
                                int result = JOptionPane.showConfirmDialog(this, modal,"Ringkasan",
                                            JOptionPane.CANCEL_OPTION,JOptionPane.PLAIN_MESSAGE);
                                if(result == JOptionPane.OK_OPTION){
                                        ringkasan = new Ringkasan();
                                        ringkasan.setJumlahART(Integer.parseInt(jumlahART.getText()));
                                        ringkasan.setJumlahARTSepuluhKeatas(Integer.parseInt(jumlahArtSepuluhKeatas.getText()));
                                        ruta.setRingkasan(ringkasan);
                                        mainScrollPane.setViewportView(new KeteranganPetugasPanel(mainScrollPane,ruta));
                                    }
                            }else{
                                    mainScrollPane.setViewportView(new KeteranganPetugasPanel(mainScrollPane, ruta));
                        }
                }
        
    }//GEN-LAST:event_nextButtonPanelMouseClicked

